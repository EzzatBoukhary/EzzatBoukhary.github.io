export function LinkIcon({ label = '', href = '', ...props }) {
  const l = label.toLowerCase();
  const u = href.toLowerCase();
  if (l.includes('github') || l === 'code') return <IconGitHub {...props} />;
  if (l.includes('youtube') || l.includes('trailer') || u.includes('youtube')) return <IconYouTube {...props} />;
  if (l.includes('devpost') || u.includes('devpost')) return <IconDevpost {...props} />;
  if (l.includes('top.gg') || l.includes('discord') || u.includes('top.gg')) return <IconDiscord {...props} />;
  if (l.includes('linkedin') || u.includes('linkedin')) return <IconLinkedIn {...props} />;
  if (l.includes('demo') || l.includes('video') || u.includes('drive.google') || u.includes('vimeo')) return <IconPlay {...props} />;
  if (l.includes('report') || l.includes('letter') || l.includes('doc') || u.includes('docs.google')) return <IconDocument {...props} />;
  return <IconGlobe {...props} />;
}
